function escape(carpark) {


  let [x, y] = findStartingPosition(carpark); // this wasn't working correctly
  let result = [];
  let verticalCount = 0;

  if (carpark.length === 1) { // needed bc if length 1, there will be no stairs [1, 0, 0..]
    let location = carpark[0].indexOf(2);
    let length = carpark[0].length - 1;
    let right = length - location;

    return (right === 0) ? [] : ['R'.concat(right)];
  }

  while (y < carpark.length) {
    let stairIdx = locateStairs(carpark, y);
    let xDiff = x - stairIdx;
    x = stairIdx;

    if (xDiff === 0 && y === carpark.length - 1 && verticalCount) {
      result.push('D'.concat(verticalCount));
    }

    if (xDiff != 0) { // horizontal
      if (verticalCount) {
        result.push('D'.concat(verticalCount));
        verticalCount = 0;
      }

      let lateral = (xDiff < 0) ? 'R'.concat(Math.abs(xDiff)) : 'L'.concat(Math.abs(xDiff));
      result.push(lateral);
    }

    verticalCount++;
    y++;
  }

  return result;
}

function findStartingPosition(carpark) {
  /*   find starting location [y axis]

    - flatten car park arrays into single array
    - find index of `2`
    - divide flat car park by length of a level until the remainder is < level length
    - this remainder (- 1) will be the index in the level
    - the # of times we divide will be the level itself where the start point is located
   */

  const copy = carpark.flat();
  let posX = copy.indexOf(2);
  let levelLength = carpark[0].length;
  let posY = 0;

  while (posX >= levelLength) { // mistake here where i had > instead of >= (doesn't account for zero)
    posX = posX - levelLength;
    posY++;
  }

  return [posX, posY];
}

function locateStairs(cp, y) {
  return (y === cp.length - 1) ? cp[y].length - 1 : cp[y].indexOf(1);
}

